class TrailerInfo {
    String id;
    String key;
    String name;
    String site;
    String size;
    String type;

    public TrailerInfo(String id, String key, String name, String site, String size, String type)
    {
        this.id = id;
        this.key = key;
        this.name = name;
        this.site = site;
        this.size = size;
        this.type = type;
    }
}
